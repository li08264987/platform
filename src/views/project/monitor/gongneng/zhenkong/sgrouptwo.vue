import $ from 'jquery'
import '@/api/monitor/svgtool'
export default {
  name: 'System',
  components: {
    infoScan,
    lineScan
  },
  data() {
    return {
      datas: {
      },
      code: '',
      linecode: ''
    }
  },
  computed: {
  },
  mounted() {
    $('svg').svgPanZoom()
    const self = this
    monitorapi.getZhenKongData({
